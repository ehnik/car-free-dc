export default function convertLineColor(line) {

  switch(line) {
    case 'Green':
        return line = 'GR'
        break;
    case 'Silver':
        return line = 'SV'
        break;
    case 'Orange':
        return line = 'OR'
        break;
    case 'Red':
        return line = 'RD'
        break;
    case 'Blue':
        return line = 'BL'
        break;
    case 'Yellow':
        return line = 'YL'
        break;
  }

}
